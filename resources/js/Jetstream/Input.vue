<template>
  <input
    ref="input"
    class="
      tw-border-gray-300
      focus:tw-border-indigo-300 focus:tw-ring focus:tw-ring-indigo-200 focus:tw-ring-opacity-50
      tw-rounded-md tw-shadow-sm
    "
    :value="modelValue"
    @input="$emit('update:modelValue', $event.target.value)"
  />
